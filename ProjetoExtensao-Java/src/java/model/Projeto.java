package model;


public class Projeto {
    
    private Integer idProj;
    private String nomeProj;
    private String objGerProj;
    private String objEspProj;
    private String dataInicio;
    private String curso;
    private String professor;

    public Integer getIdProj() {
        return idProj;
    }

    public void setIdProj(Integer idProj) {
        this.idProj = idProj;
    }
    
    public String getNomeProj() {
        return nomeProj;
    }

    public void setNomeProj(String nomeProj) {
        this.nomeProj = nomeProj;
    }

    public String getObjGerProj() {
        return objGerProj;
    }

    public void setObjGerProj(String objGerProj) {
        this.objGerProj = objGerProj;
    }

    public String getObjEspProj() {
        return objEspProj;
    }

    public void setObjEspProj(String objEspPRoj) {
        this.objEspProj = objEspPRoj;
    }

    public String getDataInicio() {
        return dataInicio;
    }

    public void setDataInicio(String dataInicio) {
        this.dataInicio = dataInicio;
    }

    public String getCurso() {
        return curso;
    }

    public void setCurso(String curso) {
        this.curso = curso;
    }

    public String getProfessor() {
        return professor;
    }

    public void setProfessor(String professor) {
        this.professor = professor;
    }
    
    
    
}
